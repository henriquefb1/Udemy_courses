# Faça um programa que solicite 3 números e faça a soma entre eles.

n1 = int(input('Primeiro número inteiro: '))
n2 = int(input('Segundo número inteiro: '))
n3 = int(input('Terceiro número inteiro: '))
r = n1 + n2 + n3

print(f'A soma de {n1, n2, n3} é {r}.')
print(f'A soma de {n1}, {n2}, {n3} é {r}.')
print(f'A soma de {n1} + {n2} + {n3} = {r}.')
